
export type QuestionType = 'SINGLE' | 'TRUE_FALSE' | 'FILL_BLANK' | 'MULTI' | 'ORDER' | 'INTERACTIVE' | 'MATCHING';
export type Difficulty = 'EASY' | 'MEDIUM' | 'HARD';

export interface QuizQuestion {
  id: number;
  category: string;
  type: QuestionType;
  difficulty: Difficulty;
  question: string;
  options: string[]; // For SINGLE/TF/MULTI/ORDER. For MATCHING: Left side items.
  correctAnswer: string | string[]; // For MATCHING: Right side items corresponding to options order.
  acceptedAnswers?: string[]; // For FILL_BLANK validation
  inputPrefix?: string; // NEW: Hint text before input
  inputSuffix?: string; // NEW: Hint text after input
  interactive?: {
    min: number;
    max: number;
    step: number;
    unit: string;
    correctValue: number;
    tolerance: number; // +/- allowed deviation
    visual?: 'COLOR_TEMP' | 'FOCAL_LENGTH' | 'SHUTTER' | 'ISO' | 'AUDIO_HZ'; // For visual feedback cues
  };
  explanation: string;
}

// 辅助函数
const q = (id: number, cat: string, diff: Difficulty, ques: string, opts: string[], ans: string, exp: string): QuizQuestion => ({
  id, category: cat, type: 'SINGLE', difficulty: diff, question: ques, options: opts, correctAnswer: ans, explanation: exp
});
const tf = (id: number, cat: string, diff: Difficulty, ques: string, ans: boolean, exp: string): QuizQuestion => ({
  id, category: cat, type: 'TRUE_FALSE', difficulty: diff, question: ques, options: ["正确 (True)", "错误 (False)"], correctAnswer: ans ? "正确 (True)" : "错误 (False)", explanation: exp
});
const fb = (id: number, cat: string, diff: Difficulty, ques: string, accepted: string[], displayAns: string, exp: string, prefix?: string, suffix?: string): QuizQuestion => ({
  id, category: cat, type: 'FILL_BLANK', difficulty: diff, question: ques, options: [], acceptedAnswers: accepted, correctAnswer: displayAns, explanation: exp, inputPrefix: prefix, inputSuffix: suffix
});
const mq = (id: number, cat: string, diff: Difficulty, ques: string, opts: string[], ans: string[], exp: string): QuizQuestion => ({
  id, category: cat, type: 'MULTI', difficulty: diff, question: ques, options: opts, correctAnswer: ans, explanation: exp
});
const oq = (id: number, cat: string, diff: Difficulty, ques: string, opts: string[], ans: string[], exp: string): QuizQuestion => ({
  id, category: cat, type: 'ORDER', difficulty: diff, question: ques, options: opts, correctAnswer: ans, explanation: exp
});
const iq = (id: number, cat: string, diff: Difficulty, ques: string, config: QuizQuestion['interactive'], displayAns: string, exp: string): QuizQuestion => ({
  id, category: cat, type: 'INTERACTIVE', difficulty: diff, question: ques, options: [], correctAnswer: displayAns, interactive: config, explanation: exp
});
const matchQ = (id: number, cat: string, diff: Difficulty, ques: string, pairs: [string, string][], exp: string): QuizQuestion => ({
  id, category: cat, type: 'MATCHING', difficulty: diff, question: ques,
  options: pairs.map(p => p[0]), // Left items
  correctAnswer: pairs.map(p => p[1]), // Right items (in correct order)
  explanation: exp
});

